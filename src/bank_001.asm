Title_Main:
    ;inc demoTimer
    ld a, [titleDemoTimer]
    add 1
    ld [titleDemoTimer], a

    ;inc demoTimer (hi) when demoTimer (lo) > $ff
    ld a, [titleDemoTimer+1]
    adc 0
    ld [titleDemoTimer+1], a

    ;allocate hi and lo
    ld a, [titleDemoTimer+1]
    ld h, a
    ld a, [titleDemoTimer]
    ld l, a

    def demoTime EQU $0708
    ;load $0708 into de
    ;this is basically a mask, $0708 is how much time is needed
    ld de, demoTime

    ;load and flip $07, turning it into $f8
    ld a, d
    cpl
    ;stash result
    ld d, a

    ;load and flip $08, turning it into $f7
    ld a, e
    cpl
    ;stash result
    ld e, a

    inc de

    ;add current timer and de
    add hl, de

    ;if bit 7 of the timer != 0, let player control
    bit 7, h
    jr nz, Title_Input
    ;else go to demo

    ;reset the timer
    xor a
    ld [titleDemoTimer], a
    ld [titleDemoTimer+1], a

    ;set demoing to 1
    ld a, 1
    ld [demoing], a

    ;start demo (?)
    ld a, $20
    call Call_000_0a84
    jr jr_001_6c24

